public class NumratMePresje {
    public static void main(String[] args) {
        float a = 10.50f; // 32 bit
        double b = 17.75; // 64 bit

        System.out.println(a);
        System.out.println(b);
        System.out.println("Variabla \"a\" e tipit \"float\", eshte 32 bit dhe ka vleren: " + a);
        System.out.println("Variabla \"b\" e tipit \"double\", eshte 64 bit dhe ka vleren: " + b);


    }
}
