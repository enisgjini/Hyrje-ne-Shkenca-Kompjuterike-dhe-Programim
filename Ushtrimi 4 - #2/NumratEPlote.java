
public class NumratEPlote {
    public static void main(String[] args) {
        byte a = 1; // 8 bit
        short b = 2; // 16 bit
        int c = 3; // 32 bit
        long d = 4; // 64 bit

        System.out.println(a);
        System.out.println("Variabla \"a\" e tipit \"byte\", eshte 8 bit dhe ka vleren: " + a);
        System.out.println("-----------------------------------");
        System.out.println(b);
        System.out.println("Variabla \"b\" e tipit \"short\", eshte 16 bit dhe ka vleren: " + b);
        System.out.println("-----------------------------------");
        System.out.println(c);
        System.out.println("Variabla \"c\" e tipit \"int\", eshte 32 bit dhe ka vleren: " + c);
        System.out.println("-----------------------------------");
        System.out.println(d);
        System.out.println("Variabla \"d\" e tipit \"long\", eshte 64 bit dhe ka vleren: " + d);
        System.out.println("-----------------------------------");

    }
}
