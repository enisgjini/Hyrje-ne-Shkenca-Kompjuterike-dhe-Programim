public class Aritmetika1 {

  public static void main(String[] args) {
    short x = 9;
    short y = 2;
    int rez1 = x + y;
    int rez2 = x - y;
    int rez3 = x * y;
    int rez4 = x / y;
    int rez5 = x % y;
    System.out.println(x + " + " + y + " = " + rez1);
    System.out.println(x + " - " + y + " = " + rez2);
    System.out.println(x + " * " + y + " = " + rez3);
    System.out.println(x + " / " + y + " = " + rez4);
    System.out.println(x + " % " + y + " = " + rez5);
  }
}
