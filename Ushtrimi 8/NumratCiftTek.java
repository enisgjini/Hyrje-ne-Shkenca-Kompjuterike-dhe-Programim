public class NumratCiftTek {
    public static void main(String[] args) {
        short a = -16;
        if (a % 2 == 0 && a > 0) {
            System.out.println(a + " është çift");
        } 
        else if (a % 2 == 0 && a > 0) {
            System.out.println(a + " është tek");
        }
        else {
            System.out.println(a + " është zero");
        }
    }
}